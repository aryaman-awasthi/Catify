package com.example.catify;

public class Image {
    private long height;
    private String id;
    private String url;
    private long width;

    public long getHeight() {
        return height;
    }

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public long getWidth() {
        return width;
    }
}